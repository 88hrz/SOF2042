    //LOAD
    void load(){
        loadTable(spService.getList());
        loadCbo(spService.getListKM());
    }
    void loadTable(ArrayList<QLSP_ViewModel> ls){
        DefaultTableModel model = (DefaultTableModel) tblSanPham.getModel();
        model.setRowCount(0);
        
        for (QLSP_ViewModel sp : ls) {
            model.addRow(new Object []{
                sp.getMaSP(), sp.getTenSP(), sp.getSoL()
                    , sp.getGiaB(), sp.getTenKM()
            });
            
        }
    }
    void loadCbo(ArrayList<KhuyenMai> ls){
        DefaultComboBoxModel model = (DefaultComboBoxModel) cboKhuyenMai.getModel();
        for (KhuyenMai km : ls) {
            model.addElement(km.getTenKM());
            
        }
    }
    
    //GETMODEL
    public SanPham getModel(){
        String maSP = txtMaSP.getText();
        String tenSP = txtTenSP.getText();
        Float giaB = Float.valueOf(txtGiaB.getText());
        String tenKM = cboKhuyenMai.getSelectedItem().toString();
        String maKM =spService.getIdByName(tenKM).getMaKM();
        
        SanPham sp = new SanPham(maSP, tenSP, giaB, maKM);
        return sp;
    }
    
    //VALIDATE
    public Boolean validateSP(){
        StringBuilder stb = new StringBuilder();
        Validate v = new Validate();
        
        v.isEmpty(txtMaSP, stb, "Ma SP dang trong!");
        v.isEmpty(txtTenSP, stb, "Ten SP dang trong!");
        v.isEmpty(txtGiaB, stb, "Gia ban dang trong!");
        
        if (stb.length()>0) {
            JOptionPane.showMessageDialog(this, stb);
            return false;
        }else{
            return true;
        }
    }
    
    //CLEAR
    void clearForm(){
        txtMaSP.setText("");
        txtTenSP.setText("");
        txtGiaB.setText("");
    }

