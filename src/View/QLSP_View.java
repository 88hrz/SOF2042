    private void tblSanPhamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSanPhamMouseClicked
        // CLICK
        int pos = tblSanPham.getSelectedRow();
        QLSP_ViewModel sp = spService.getList().get(pos);
        txtMaSP.setText(sp.getMaSP());
        txtTenSP.setText(sp.getTenSP());
        txtGiaB.setText(sp.getGiaB().toString());
        cboKhuyenMai.setSelectedItem(sp.getTenKM());
    }//GEN-LAST:event_tblSanPhamMouseClicked

    private void btnAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddMouseClicked
        // ADD
        int result = JOptionPane.showConfirmDialog(this, "Ban muon them SP?","NOTI", JOptionPane.YES_NO_OPTION);
        String maSP = txtMaSP.getText().trim();
        if (spService.checkId(maSP)) {
            JOptionPane.showMessageDialog(this,"Ma SP bi trung, khong the them!");
            clearForm();
        }else{
            if (validateSP() && result == JOptionPane.YES_OPTION) {
                JOptionPane.showMessageDialog(this, spService.addSP(getModel()));
                loadTable(spService.getList());
                clearForm();
            }else{
                JOptionPane.showMessageDialog(this, "Da huy thao tac them SP!");
            }
        }
    }//GEN-LAST:event_btnAddMouseClicked

